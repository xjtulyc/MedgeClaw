class TestScoringFileParser:
    """Tests for PGS Catalog scoring file parsing."""

    def test_parse_scoring_file(self):
        """parse_scoring_file returns all 5 variants with correct fields."""
        variants = gwas_prs.parse_scoring_file(
            FIXTURES / "mock_scoring_file.txt"
        )
        assert len(variants) == 5
        first = variants[0]
        assert first["rsid"] == "rs100001"
        assert first["effect_allele"] == "A"
        assert float(first["effect_weight"]) == pytest.approx(0.15)

    def test_parse_scoring_file_metadata(self):
        """Metadata comment lines (starting with #) are not returned as variants."""
        variants = gwas_prs.parse_scoring_file(
            FIXTURES / "mock_scoring_file.txt"
        )
        # Only data rows, no comment lines
        for v in variants:
            assert not v["rsid"].startswith("#")

    def test_parse_scoring_file_missing_rsid(self, tmp_path):
        """Rows without a valid rsID are skipped."""
        score_file = tmp_path / "no_rsid.txt"
        score_file.write_text(
            "#pgs_id=PGS000099\n"
            "rsID\tchr_name\tchr_position\teffect_allele\tother_allele\teffect_weight\n"
            "rs100001\t1\t100000\tA\tG\t0.15\n"
            "\t2\t200000\tT\tC\t0.08\n"
            ".\t3\t300000\tG\tA\t-0.12\n"
        )
        variants = gwas_prs.parse_scoring_file(score_file)
        assert len(variants) == 1
        assert variants[0]["rsid"] == "rs100001"


