class TestPRSCalculation:
    """Tests for the core PRS calculation logic."""

    def test_calculate_prs_basic(self):
        """Known genotypes + weights produce a non-zero raw PRS."""
        _fmt, _n, genotypes = gwas_prs.parse_genotype_file(
            FIXTURES / "mock_genotype_23andme.txt"
        )
        variants = gwas_prs.parse_scoring_file(
            FIXTURES / "mock_scoring_file.txt"
        )
        result = gwas_prs.calculate_prs(genotypes, variants)
        assert "raw_score" in result
        assert isinstance(result["raw_score"], float)
        assert result["raw_score"] != 0.0

    def test_calculate_prs_overlap(self):
        """Overlap fraction is correctly calculated."""
        _fmt, _n, genotypes = gwas_prs.parse_genotype_file(
            FIXTURES / "mock_genotype_23andme.txt"
        )
        variants = gwas_prs.parse_scoring_file(
            FIXTURES / "mock_scoring_file.txt"
        )
        result = gwas_prs.calculate_prs(genotypes, variants)
        # All 5 scoring variants are present in the genotype file
        assert result["variants_used"] == 5
        assert result["variants_total"] == 5
        assert result["overlap_fraction"] == pytest.approx(1.0)

    def test_calculate_prs_no_overlap(self):
        """Zero score when no scoring SNPs match the genotype file."""
        genotypes = {"rs999999": "AA"}
        variants = gwas_prs.parse_scoring_file(
            FIXTURES / "mock_scoring_file.txt"
        )
        result = gwas_prs.calculate_prs(genotypes, variants)
        assert result["variants_used"] == 0
        assert result["raw_score"] == 0.0

    def test_calculate_prs_partial_overlap(self):
        """Subset of SNPs match — only matched ones contribute."""
        genotypes = {
            "rs100001": "AG",
            "rs100003": "AA",
        }
        variants = gwas_prs.parse_scoring_file(
            FIXTURES / "mock_scoring_file.txt"
        )
        result = gwas_prs.calculate_prs(genotypes, variants)
        assert result["variants_used"] == 2
        assert result["variants_total"] == 5
        assert result["overlap_fraction"] == pytest.approx(0.4)


