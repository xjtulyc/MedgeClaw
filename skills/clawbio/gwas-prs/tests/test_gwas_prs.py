class TestPGSCatalogClient:
    """Tests for PGS Catalog REST API client (mocked)."""

    def test_search_traits(self, tmp_path):
        """search_traits returns a list of matching trait dicts."""
        mock_data = json.loads(
            (FIXTURES / "mock_trait_search.json").read_text()
        )
        mock_response = MagicMock()
        mock_response.status_code = 200
        mock_response.json.return_value = mock_data

        with patch.object(gwas_prs.requests.Session, "get", return_value=mock_response):
            client = gwas_prs.PGSCatalogClient(
                cache_dir=tmp_path / "cache", use_cache=False
            )
            traits = client.search_traits("diabetes")
            assert len(traits) == 2
            assert traits[0]["id"] == "EFO_0001360"

    def test_get_score_metadata(self, tmp_path):
        """get_score_metadata returns a score dict with expected keys."""
        mock_data = json.loads(
            (FIXTURES / "mock_score_metadata.json").read_text()
        )
        mock_response = MagicMock()
        mock_response.status_code = 200
        mock_response.json.return_value = mock_data

        with patch.object(gwas_prs.requests.Session, "get", return_value=mock_response):
            client = gwas_prs.PGSCatalogClient(
                cache_dir=tmp_path / "cache", use_cache=False
            )
            score = client.get_score_metadata("PGS000013")
            assert score["id"] == "PGS000013"
            assert score["variants_number"] == 8

    def test_rate_limiting(self, tmp_path):
        """_throttle introduces a delay between rapid calls."""
        client = gwas_prs.PGSCatalogClient(
            cache_dir=tmp_path / "cache", use_cache=False
        )
        client._last_request_time = time.time()
        t0 = time.time()
        client._throttle()
        elapsed = time.time() - t0
        # Should have waited at least some time (rate limit interval)
        assert elapsed >= 0.4


