class TestGenotypeParser:
    """Tests for genotype file format detection and parsing."""

    def test_detect_23andme_format(self):
        """detect_format identifies 23andMe-style header lines."""
        lines = [
            "# This data file generated by 23andMe",
            "#",
            "# rsid\tchromosome\tposition\tgenotype",
            "rs100001\t1\t100000\tAG",
        ]
        fmt = gwas_prs.detect_format(lines)
        assert fmt == "23andme"

    def test_detect_ancestry_format(self):
        """detect_format identifies AncestryDNA-style header lines."""
        lines = [
            "#AncestryDNA raw data download",
            "RSID,CHROMOSOME,POSITION,ALLELE1,ALLELE2",
            "rs100001,1,100000,A,G",
        ]
        fmt = gwas_prs.detect_format(lines)
        assert fmt == "ancestry"

    def test_parse_23andme_file(self):
        """parse_genotype_file returns all valid SNPs from a 23andMe file."""
        fmt, total, genotypes = gwas_prs.parse_genotype_file(
            FIXTURES / "mock_genotype_23andme.txt"
        )
        assert fmt == "23andme"
        assert total == 6
        assert "rs100001" in genotypes
        assert genotypes["rs100001"] == "AG"

    def test_parse_genotype_skips_invalid(self, tmp_path):
        """Lines with '--' genotypes are filtered; '00' and valid genotypes are kept.

        The shared parser (clawbio.common.parsers) strips '--' entries but
        preserves '00' (ambiguous no-calls that downstream scoring handles
        by yielding dosage 0). Valid genotypes are always kept.
        """
        bad_file = tmp_path / "bad_geno.txt"
        bad_file.write_text(
            "# This data file generated by 23andMe\n"
            "#\n"
            "# rsid\tchromosome\tposition\tgenotype\n"
            "rs000001\t1\t100000\t--\n"
            "rs000002\t2\t200000\t00\n"
            "rs000003\t3\t300000\tAG\n"
        )
        fmt, total, genotypes = gwas_prs.parse_genotype_file(bad_file)
        assert "rs000001" not in genotypes  # '--' is filtered
        assert "rs000003" in genotypes      # valid genotype kept
        assert total >= 1


